package com.ramadini.tutorialmakeup.Makeup;

public class ItemObjectMakeup {
    public String Nama, Kategori, Eyebrow, Foundation, Bedak, Eyeshadow, Highlighter, Blushon, Shading,
            Lipstik, Mascara, Eyeliner;
    public int img;


    public ItemObjectMakeup(String Nama, String Kategori, String Eyebrow, String Foundation,
                            String Bedak, String Eyeshadow, String Highlighter, String Blushon,
                            String Shading, String Lipstik, String Mascara, String Eyeliner, int img){
        this.Nama = Nama;
        this.Kategori = Kategori;
        this.Eyebrow = Eyebrow;
        this.Foundation = Foundation;
        this.Bedak = Bedak;
        this.Eyeshadow = Eyeshadow;
        this.Highlighter = Highlighter;
        this.Blushon = Blushon;
        this.Shading = Shading;
        this.Lipstik = Lipstik;
        this.Mascara = Mascara;
        this.Eyeliner = Eyeliner;
        this.img = img;

    }

}
